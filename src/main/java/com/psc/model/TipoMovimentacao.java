package com.psc.model;

/**
 * Enumeração que representa os tipos de movimentação de estoque possíveis.
 *
 * <p>Define se a movimentação é uma entrada (adição) ou uma saída (remoção) de produtos no estoque.</p>
 *
 * @author EvelynYs2
 * @version 1.0
 */
public enum TipoMovimentacao {
    /**
     * Representa uma entrada de produtos no estoque.
     */
    ENTRADA,

    /**
     * Representa uma saída de produtos do estoque.
     */
    SAIDA
}
