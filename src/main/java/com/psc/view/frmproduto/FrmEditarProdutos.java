    private void JBAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBAlterarActionPerformed
        // TODO add your handling code here:
        try {
            int id = Integer.parseInt(this.JTFId.getText());
            String nome = this.JTFNome.getText();
            double precoUnitario = Double.parseDouble(this.JTFpreco.getText());
            String unidade = (String) this.JCBUnidade.getSelectedItem();
            int quantidadeEstoque = Integer.parseInt(this.JTFatual.getText());
            int quantidadeMinima = Integer.parseInt(this.JTFminima.getText());
            int quantidadeMaxima = Integer.parseInt(this.JTFmaxima.getText());
            String categoria = this.JCBCategoria.getSelectedItem().toString();
            int categoriaId = 0;
            String categoriaNome = "";

            // Validações simples
            if (nome.length() < 2) {
                throw new IllegalArgumentException("Nome deve conter ao menos 2 caracteres.");
            }
            if (precoUnitario <= 0) {
                throw new IllegalArgumentException("Preço unitário deve ser maior que zero.");
            }

            if (quantidadeEstoque < 0 || quantidadeMinima < 0 || quantidadeMaxima < 0) {
                throw new IllegalArgumentException("As quantidades não podem ser negativas.");
            }

            if (categoria != null && categoria.contains(" - ")) {
                String[] partes = categoria.split(" - ", 2); // limita a 2 partes
                categoriaId = Integer.parseInt(partes[0].trim());
                categoriaNome = partes[1].trim();
            }

            Categoria cat = new Categoria(categoriaId, categoriaNome);

            // Criação do produto
            Produto p = new Produto(id, nome, precoUnitario, unidade, quantidadeEstoque, quantidadeMinima, quantidadeMaxima, cat);

            // Atualiza no banco de dados
            produtoDAO.atualizar(p);
            carregarTabela();

            JOptionPane.showMessageDialog(null, "Produto atualizado com sucesso!");

            // Limpa os campos após cadastro
            this.JTFId.setText("");
            this.JTFNome.setText("");
            this.JTFpreco.setText("");
            this.JTFatual.setText("");
            this.JTFminima.setText("");
            this.JTFmaxima.setText("");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Preencha os campos numéricos corretamente.", "Erro", JOptionPane.ERROR_MESSAGE);
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        } finally {
            carregarTabela(); // Recarrega a tabela com os dados atualizados
        }
    }//GEN-LAST:event_JBAlterarActionPerformed


    private void JBApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBApagarActionPerformed
        // TODO add your handling code here:
        try {
            int id = 0;

            if (this.TableProdutos.getSelectedRow() == -1) {
                throw new Exception("Primeiro selecione um Produto para APAGAR");
            } else {
                id = Integer.parseInt(this.TableProdutos
                        .getValueAt(this.TableProdutos.getSelectedRow(), 0).toString());
            }

            int respostaUsuario = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja apagar este Produto?");

            if (respostaUsuario == 0) { // clicou em SIM

                if (this.produtoDAO.deletar(id)) {
                    // Limpa os campos
                    this.JTFId.setText("");
                    this.JTFNome.setText("");
                    this.JTFpreco.setText("");
                    this.JTFatual.setText("");
                    this.JTFminima.setText("");
                    this.JTFmaxima.setText("");

                    JOptionPane.showMessageDialog(null, "Produto apagado com sucesso!");
                }
            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, erro.getMessage());
        } finally {
            carregarTabela(); // Recarrega a tabela com os dados atualizados
        }
    }//GEN-LAST:event_JBApagarActionPerformed

